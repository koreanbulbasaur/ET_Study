int_n = int(input("정수를 입력해주세요: "))

if (int_n % 2) == 0:
    print(f"{int_n}은 2로 나누어 떨어지는 숫자입니다.")
else:
    print(f"{int_n}은 2로 나누어 떨어지는 숫자가 아닙니다.")
    
if (int_n % 3) == 0:
    print(f"{int_n}은 3로 나누어 떨어지는 숫자입니다.")
else:
    print(f"{int_n}은 3로 나누어 떨어지는 숫자가 아닙니다.")
    
if (int_n % 4) == 0:
    print(f"{int_n}은 4로 나누어 떨어지는 숫자입니다.")
else:
    print(f"{int_n}은 4로 나누어 떨어지는 숫자가 아닙니다.")
    
if (int_n % 5) == 0:
    print(f"{int_n}은 5로 나누어 떨어지는 숫자입니다.")
else:
    print(f"{int_n}은 5로 나누어 떨어지는 숫자가 아닙니다.")